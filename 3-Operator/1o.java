class Demo{                                 //Arithematic Operator
    public static void main(String [] args){
        
        int x = 10;
        int y = 20;
                                   
        System.out.println(x+y);   //30
        System.out.println(x-y);   //-10
        System.out.println(x*y);   //200
        System.out.println(x/y);   //0 Any arithematic operation on numbers gives int output therefor take integer part of 0.5
        System.out.println(x%y);   //10

    }
}