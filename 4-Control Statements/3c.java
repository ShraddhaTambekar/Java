class Demo{
    public static void main(String []args){

        float temp = 19.65f;

        if(temp>50){                         

            System.out.println("High temprature");

        }else if(temp>=30 && temp<=50){

            System.out.println("Medium Temprature");

        }else{

            System.out.println("low temprature");
        }

    }
}

