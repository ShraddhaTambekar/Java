/*
   while loop control statement
*/

class Demo{ 
    public static void main(String []args){
       
        int i = 1;                             //Initialisation

        while(i<=5){                           //Condition                       
            System.out.println("Hello");       //Body
            i++;                               //Increment/Decrement
        }    

    }
}

/*
 output-
        Hello
        Hello
        Hello
        Hello
        Hello
 */

